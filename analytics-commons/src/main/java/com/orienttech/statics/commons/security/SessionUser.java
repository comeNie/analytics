package com.orienttech.statics.commons.security;

import java.io.Serializable;

/**
 * Session中的User信息
 */
public class SessionUser implements Serializable {

	private static final long serialVersionUID = 5424746916457281847L;
	private Long id;
	private String loginName;
	private String userName;
	private Long orgId;
	private String orgCode;
	private String orgName;
	private String roleId;
	private String premLevel;//权限级别1：表示总部，2：表示子公司和子机构
	
	private String otherOrgCode;//其他机构
	private String otherOrgName;
	
	public String getOtherOrgCode() {
		return otherOrgCode;
	}
	public void setOtherOrgCode(String otherOrgCode) {
		this.otherOrgCode = otherOrgCode;
	}
	public String getOtherOrgName() {
		return otherOrgName;
	}
	public void setOtherOrgName(String otherOrgName) {
		this.otherOrgName = otherOrgName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	public Long getOrgId() {
		return orgId;
	}
	public void setOrgId(Long orgId) {
		this.orgId = orgId;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getPremLevel() {
		return premLevel;
	}
	public void setPremLevel(String premLevel) {
		this.premLevel = premLevel;
	}

}
